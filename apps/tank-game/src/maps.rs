//! Pre-made maps, described as ASCII art and parsed into a [`GameMap`].
//!
//! Legend:
//! - `.` grass
//! - `~` water
//! - `^` mountain / rock
//! - `o` ore (light, 50)
//! - `O` ore (rich, 150)
//! - `P` player start (grass)
//! - `E` enemy start (grass)

use crate::grid::{GameMap, Terrain, Tile};

pub struct MapData {
    pub map: GameMap,
    pub player_start: Tile,
    pub enemy_start: Tile,
}

/// All bundled maps, by name.
pub fn all_maps() -> Vec<(&'static str, &'static str)> {
    vec![
        ("Twin Bases", TWIN_BASES),
        ("River Divide", RIVER_DIVIDE),
    ]
}

pub fn load_map(ascii: &str) -> MapData {
    let rows: Vec<&str> = ascii.lines().filter(|l| !l.trim().is_empty()).collect();
    let height = rows.len();
    let width = rows.iter().map(|r| r.chars().count()).max().unwrap_or(0);

    let mut map = GameMap::new(width, height);
    let mut player_start = (3, 3);
    let mut enemy_start = (width as i32 - 4, height as i32 - 4);

    for (row, line) in rows.iter().enumerate() {
        for (col, ch) in line.chars().enumerate() {
            let (c, r) = (col as i32, row as i32);
            match ch {
                '~' => map.set_terrain(c, r, Terrain::Water),
                '^' => map.set_terrain(c, r, Terrain::Mountain),
                'o' => map.set_ore(c, r, 50),
                'O' => map.set_ore(c, r, 150),
                'P' => player_start = (c, r),
                'E' => enemy_start = (c, r),
                _ => map.set_terrain(c, r, Terrain::Grass),
            }
        }
    }

    MapData { map, player_start, enemy_start }
}

